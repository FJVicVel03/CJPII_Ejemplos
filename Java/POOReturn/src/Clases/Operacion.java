package Clases;

public class Operacion {

    public int Suma(int a, int b)
    {
        int suma = a + b;
        return suma;
    }
    public int Restar(int a, int b)
    {
        int resta = a - b;
        return resta;
    }
    public int Multiplicacion(int a, int b)
    {
        int mult = a * b;
        return mult;
    }
    public int Division(int a, int b)
    {
        int div = a / b;
        return div;
    }

}
